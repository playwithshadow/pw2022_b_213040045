<?php
// Materi Pertemuan 3
// Pengulangan :
// for
// while
// do.. while
// foreach : pengulangan khusus array

// Ini menggunakan for
for($i = 0; $i < 5; $i++ ) {
    echo "Hello World! ";
    echo "<br>";
}
    echo "<hr>";

// ini menggunakan while
$i = 10;
while( $i < 5 ) {
    echo "Hello World!";
    echo "<br>";
$i++;
}
    echo"<hr>";

// ini menggunakan do .. while
$i = 10;
do {    
    echo "Hello World";
    echo "<br>";
$i++; 
} while ( $i < 5 );

?>